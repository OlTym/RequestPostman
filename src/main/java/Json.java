public class Json {

    private String foo1;
    private String foo2;


    public String getFoo1() {
        return foo1;
    }

    public String getFoo2() {
        return foo2;
    }

    @Override
    public String toString() {
        return "Json{" +
                "foo1='" + foo1 + '\'' +
                ", foo2='" + foo2 + '\'' +
                '}';
    }

}
